#include "fillit.h"

int		ft_sqrt(int nb)
{
	int a;

	a = 0;
	if (nb <= 0)
		return (0);
	if (nb > 0)
	{
		while (nb > a * a)
			a++;
		if (a * a == nb)
			return (a);
	}
	return (--a);
}
