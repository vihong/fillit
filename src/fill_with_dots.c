#include "fillit.h"

void	fill_with_dots(char **sq, int sq_size)
{
	int i;
	int j;

	i = 0;
	while (i < sq_size)
	{
		j = 0;
		while (j < sq_size)
		{
			sq[i][j] = '.';
			j++;
		}
		i++;
	}
}
